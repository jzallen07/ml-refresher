def list_diagrams() -> list[dict]:
    return [
        {"type": k, "title": v["title"]}
        for k, v in DIAGRAMS.items()
    ]


def render(diagram_type: str, annotations: dict[str, str] | None = None) -> dict | None:
    entry = DIAGRAMS.get(diagram_type)
    if not entry:
        return None

    merged = {**DEFAULTS.get(diagram_type, {}), **(annotations or {})}
    text = entry["diagram"]

    # Substitute {key} placeholders
    for key, val in merged.items():
        text = text.replace(f"{{{key}}}", val)

    return {
        "type": diagram_type,
        "title": entry["title"],
        "rendered": text,
        "annotations": merged,
    }
